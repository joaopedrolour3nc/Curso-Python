# Operdadores Aritméticos

#Adição
adicao = 10 + 10
print('Adição: ', adicao)   

# Subtração
subtracao = 10 -5
print("Subtração: ", subtracao)

# Multiplicação
multiplicacao = 10 * 6
print("Multiplicação: ", multiplicacao)

# Divisão
divisao = multiplicacao / 2
print('Divisão: ' ,divisao)

# Divisão Inteira 
divisao_inteira = 10 // 2.2
print('Divisão Inteira: ' ,divisao_inteira)

# Exponenciação
exponenciacao = 2 ** 10
print ('Exponenciação', exponenciacao)

# Módulo
# Em outras palavras, O resto da divisão
modulo = 25 % 5
print('Módulo', modulo)

